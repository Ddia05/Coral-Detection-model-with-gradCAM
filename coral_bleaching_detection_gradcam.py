import gradio as gr

def predict_with_heatmap(uploaded_img):
    try:
        print("Starting prediction...")
        img = Image.open(uploaded_img).convert("RGB")
        print("Image loaded.")

        transformed = transform(img).unsqueeze(0).to(device)
        print("Image transformed.")
        model.eval()
        output = model(transformed)
        pred_class = output.argmax(dim=1).item()
        print(f"Predicted class: {pred_class}")

        cam_map = gradcam.generate(transformed)
        print("Grad-CAM generated.")

        img_np = transformed.squeeze().permute(1, 2, 0).cpu().numpy()
        img_np = (img_np - img_np.min()) / (img_np.max() - img_np.min())

        heatmap = cv2.applyColorMap(np.uint8(255 * cam_map), cv2.COLORMAP_JET)
        heatmap = cv2.cvtColor(heatmap, cv2.COLOR_BGR2RGB) / 255.0
        overlay = 0.4 * heatmap + 0.6 * img_np
        print("Overlay generated.")

        cam_map_img = np.uint8(255 * cam_map)
        cam_map_img = cv2.cvtColor(cam_map_img, cv2.COLOR_GRAY2RGB)  
        overlay = np.clip(overlay, 0, 1)
        overlay_img = (overlay * 255).astype(np.uint8)
        
        img_np_disp = (img_np * 255).astype(np.uint8)

        return (pred_class, img_np_disp, cam_map_img, overlay_img)


    except Exception as e:
        print("Error:", e)
        return "Error", None, None, None

